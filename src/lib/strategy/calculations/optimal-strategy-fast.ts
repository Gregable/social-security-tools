/**
 * Fast per-cell optimal-filing-age solver for a couple.
 *
 * `optimalStrategyCoupleFast` is a drop-in replacement for
 * `optimalStrategyCouple` / `optimalStrategyCoupleOptimized` that produces
 * identical results (exact NPV match) in a fraction of the time.
 *
 * ## Problem
 *
 * Given a couple with known (fixed) death dates, find the (earnerFiling,
 * depFiling) pair that maximizes NPV of lifetime benefits. This is called
 * once per grid cell in the StrategyMatrix UI (~14×14 cells).
 *
 * The slow reference iterates F×F filing pairs (F ≈ 96 months) and calls
 * `strategySumCentsCouple`, which re-walks the benefit periods and allocates
 * fresh `BenefitPeriod`/`MonthDate`/`Money` objects per pair.
 *
 * ## Optimizations
 *
 * 1. Raw numbers throughout. Filing dates are epoch-months (Int32), amounts
 *    are cents (Float64). No allocations in the hot loop.
 * 2. Pre-tabulated discount-factor table `dkF[k] = (1+r)^-k` covering the
 *    entire range from currentDate to max(earnerDeath, depDeath). Replaces
 *    `Math.pow` calls with O(1) array lookups.
 * 3. Per-filing-age benefit amounts (filing-year and post-January) are
 *    pre-tabulated per recipient (nEF + nDF values).
 * 4. Earner personal NPV is independent of dep filing; precomputed once per
 *    earner filing age.
 * 5. Dep personal NPV (full and truncated-at-earnerDeath) is independent of
 *    earner filing age in the common `!depZeroPia` case; precomputed once
 *    per dep filing age.
 *
 * The inner (fe, fd) loop does O(1) arithmetic + two small helper calls
 * (spousal/survivor amount) + a constant-amount NPV via discount-factor
 * differences.
 *
 * ## Correctness
 *
 * Arithmetic matches `strategySumPeriodsCouple` in strategy-calc.ts to the
 * last rounding step. Validated against 1000 golden cases generated by the
 * slow reference; see `src/test/strategy/grid-optimal-goldens.test.ts`.
 */

import { eligibleForSpousalBenefit } from '$lib/benefit-calculator';
import { type MonthDate, MonthDuration } from '$lib/month-time';
import type { Recipient } from '$lib/recipient';
import { classifyEarnerDependent } from './earner-dependent.js';
import {
  calculateMonthlyDiscountRate,
  earliestFiling,
} from './strategy-calc.js';

